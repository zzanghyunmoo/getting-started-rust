//! Example 3: Println Basics
//!
//! println! 매크로의 기본 사용법을 학습합니다.
//!
//! Demonstrates:
//! - 간단한 문자열 출력
//! - 여러 줄 출력
//! - 빈 줄 출력

fn main() {
    // 간단한 문자열 출력
    println!("Hello, world!");

    // 여러 줄 출력
    println!("첫 번째 줄");
    println!("두 번째 줄");

    // 빈 줄 출력
    println!(); // 빈 줄
    println!("빈 줄 후에 출력");
}
